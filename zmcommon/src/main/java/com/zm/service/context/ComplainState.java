package com.zm.service.context;

public class ComplainState {

	private int uid;
	
	private int beComplainedNum;
	
	private int ComplainNum;

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public int getBeComplainedNum() {
		return beComplainedNum;
	}

	public void setBeComplainedNum(int beComplainedNum) {
		this.beComplainedNum = beComplainedNum;
	}

	public int getComplainNum() {
		return ComplainNum;
	}

	public void setComplainNum(int complainNum) {
		ComplainNum = complainNum;
	}
	
}
